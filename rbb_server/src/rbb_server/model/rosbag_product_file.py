from sqlalchemy import *
from sqlalchemy.orm import relationship

from .base import Base


class RosbagProductFile(Base):
    __tablename__ = "rosbag_product_file"
    product_id = Column(Integer, ForeignKey('rosbag_product.uid'), primary_key=True)
    file_id = Column(Integer, ForeignKey('file.uid'), primary_key=True)
    key = Column(String(50))

    # Relationships
    file = relationship("File")

